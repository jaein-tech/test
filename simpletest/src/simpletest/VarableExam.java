package simpletest;

public class VarableExam {

	public static void main(String[] args) {
		int count;
//		int 정수만 담을 수 있다.
		count = 10;
		
		count = 20;
		
		System.out.println(count);
		
//		double 소수점까지 담을 수 있다.
		
		double avg = 11.1;
		
		System.out.println(avg);
		
		String name = "John";
//		String 문자열
		System.out.println(name);
		
//		두 단어이상 첫단어 뒤에 오는 단어 첫글자는 대문자 예) simpleCount
	}

}
