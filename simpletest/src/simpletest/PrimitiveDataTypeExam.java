package simpletest;

public class PrimitiveDataTypeExam {

	public static void main(String[] args) {
		boolean isFun = true;
		System.out.println(isFun);
		
//		char 타입은 문자 단 하나만 표시가능 작은 따옴표 사용
		char a = 'k';
		
		System.out.println(a);
		
//		큰 정수를 표현하려면 long이라는 데이터 타입사용, 뒤에 반드시 L붙이기
		long g = 65184651L;
		
//		float 실수를 표현하는데 뒤에 f를 붙인다.
		float f = 14.5f;
		
//		double로 조금 더 큰 실수를 표현한다.

	}

}
